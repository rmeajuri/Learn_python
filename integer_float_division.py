float_division = 12 / 3 #prints division result with .0
print (float_division)

integer_division = 12 // 3. #prints division result without .0 (floating point). Also Known as Floor Division
print(integer_division)

integer1_division = 12 // 5  #prints division result without .0 (floating point) & rounds off the number.
print(integer1_division)


